    /**
      +----------------------------------------------------------
     * 获取控制器文件名
      +----------------------------------------------------------
     * @access public 
      +----------------------------------------------------------
     * @return string
      +----------------------------------------------------------
     */
    public function getFile();

    /**
      +----------------------------------------------------------
     * 获取控制器类名
      +----------------------------------------------------------
     * @access public 	
      +----------------------------------------------------------
     * @return string
      +----------------------------------------------------------
     */
    public function getModule();

    /**
      +----------------------------------------------------------
     * 获取请求的操作
      +----------------------------------------------------------
     * @access public 
      +----------------------------------------------------------
     * @return string
      +----------------------------------------------------------
     */
    public function getAction();
