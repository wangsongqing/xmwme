interface ICache
{
	/**
	 +----------------------------------------------------------
	 * 写入缓存
	 +----------------------------------------------------------
	 * @access public
	 +----------------------------------------------------------
	 * @param string  $key    缓存键值
	 * @param mixed   $value  被缓存的数据
	 +----------------------------------------------------------
	 * @return boolean
	 +----------------------------------------------------------     
	 */
	public function set($key,$value);

	/**
	 +----------------------------------------------------------
	 * 读取缓存
	 +----------------------------------------------------------
	 * @access public 
	 +----------------------------------------------------------
	 * @param string $key 缓存键值
	 +----------------------------------------------------------
	 * @return mixed
	 +----------------------------------------------------------
	 */
	public function get($key);

	/**
	 +----------------------------------------------------------
	 * 删除缓存
	 +----------------------------------------------------------
	 * @access public 
	 +----------------------------------------------------------
	 * @param  string $key 缓存键值
	 +----------------------------------------------------------
	 * @return boolean
	 +----------------------------------------------------------
	 */
	public function remove($key);

	/**
	 +----------------------------------------------------------
	 * 清除缓存(删除所有缓存数据)
	 +----------------------------------------------------------
	 * @access public 
	 +----------------------------------------------------------
	 * @return boolean
	 +----------------------------------------------------------
	 */
	public function clear();

}
?>